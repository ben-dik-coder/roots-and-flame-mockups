function rfGetLang() {
  return localStorage.getItem('rf-lang') || 'no';
}

function rfSetLang(lang) {
  localStorage.setItem('rf-lang', lang);
  document.documentElement.lang = lang;
  rfApplyTranslations(lang);
  document.querySelectorAll('.lang-toggle button').forEach((btn) => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

function rfApplyTranslations(lang) {
  const strings = RF_I18N[lang] || RF_I18N.no;
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    if (strings[key] !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = strings[key];
      } else {
        el.textContent = strings[key];
      }
    }
  });
  document.querySelectorAll('[data-i18n-alt]').forEach((el) => {
    const key = el.dataset.i18nAlt;
    if (strings[key] !== undefined) {
      el.alt = strings[key];
    }
  });
}

function rfInitI18n() {
  const lang = rfGetLang();
  document.documentElement.lang = lang;
  rfApplyTranslations(lang);
  document.querySelectorAll('.lang-toggle button').forEach((btn) => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
    btn.addEventListener('click', () => rfSetLang(btn.dataset.lang));
  });
}

function rfInitNav() {
  const toggle = document.querySelector('.nav-toggle');
  const links = document.querySelector('.nav-links');
  if (toggle && links) {
    toggle.addEventListener('click', () => links.classList.toggle('open'));
    links.querySelectorAll('a').forEach((a) => {
      a.addEventListener('click', () => links.classList.remove('open'));
    });
  }
}

document.addEventListener('DOMContentLoaded', () => {
  rfInitI18n();
  rfInitNav();
});
